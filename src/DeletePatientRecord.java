    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String patientID = txtSearch.getText().trim();

        if (patientID.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a valid Patient ID");
            return;
        }

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();

            // Query to join tables and filter by patientID
            String query = "SELECT * FROM patient " +
            "LEFT JOIN patientreport ON patient.patientId = patientreport.patientId " +
            "WHERE patient.patientId = '" + patientID + "'";

            ResultSet rs = st.executeQuery(query);

            // Check if result is empty
            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(null, "No records found for Patient ID: " + patientID);
            } else {
                tablePatientRecord.setModel(DbUtils.resultSetToTableModel(rs));
                tablePatientRecord.setAutoResizeMode(tablePatientRecord.AUTO_RESIZE_OFF);
                JOptionPane.showMessageDialog(null, "Search Successful");
            }

            rs.close();
            st.close();
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

