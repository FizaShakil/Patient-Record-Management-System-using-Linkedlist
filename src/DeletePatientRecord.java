    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String patientID = txtSearch.getText().trim();

    if (patientID.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter a Patient ID to delete");
        return;
    }

    int confirmation = JOptionPane.showConfirmDialog(null, 
        "Are you sure you want to delete this record?", 
        "Confirm Deletion", JOptionPane.YES_NO_OPTION);

    if (confirmation == JOptionPane.YES_OPTION) {
        try {
            Connection con = ConnectionProvider.getCon();

            // Delete patient records from both tables
            String deletePatientReportQuery = "DELETE FROM patientreport WHERE patientId = ?";
            String deletePatientQuery = "DELETE FROM patient WHERE patientId = ?";

            PreparedStatement pst1 = con.prepareStatement(deletePatientReportQuery);
            PreparedStatement pst2 = con.prepareStatement(deletePatientQuery);

            pst1.setString(1, patientID);
            pst2.setString(1, patientID);

            pst1.executeUpdate();
            pst2.executeUpdate();

            JOptionPane.showMessageDialog(null, "Patient record deleted successfully");

            pst1.close();
            pst2.close();
            con.close();

            // Clear the search text and reset the table model
            txtSearch.setText("");
            tablePatientRecord.setModel(new javax.swing.table.DefaultTableModel());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

